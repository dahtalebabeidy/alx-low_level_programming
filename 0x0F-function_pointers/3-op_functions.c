#include "3-calc.h"

/**
* op_add - adds two integers
*
* @a: parametre
* @b: parametre
*
* Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - sub two integers
*
* @a: parametre
* @b: parametre
*
* Return: sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - mult two integers
*
* @a: parametre
* @b: parametre
*
* Return: mul
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - div two integers
*
* @a: parametre
* @b: parametre
*
* Return: div
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - mod two integers
*
* @a: parametre
* @b: parametre
*
* Return: mod
*/

int op_mod(int a, int b)
{
	return (a % b);
}
